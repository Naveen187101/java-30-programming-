package Swaping;

public class Swap3Without3rdV {

	public static void main(String[] args) {
		int a = 20;
		int b = 10;
		
		// using * and / without 3rd variable
		a=a*b;//20*10=300
		b=a/b;//300/10=30
		a=a/b;//300/30=10
		System.out.println("value of a:"+a+" "+"value of b:"+b);


	}

}
